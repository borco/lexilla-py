// Wraps a single ILexer5* returned by CreateLexer(). Owns it (and calls
// Release() on destruction) until detach() hands ownership to a Scintilla
// editor via SCI_SETILEXER -- see docs/specs/mission.md's "Cross-binding
// integration" decision.
class Lexer {
public:
    explicit Lexer(ILexer5 *lexer) noexcept : lexer_(lexer) {}
    ~Lexer() { ReleaseIfOwned(); }

    Lexer(const Lexer &) = delete;
    Lexer &operator=(const Lexer &) = delete;

    std::uintptr_t Pointer() const { return reinterpret_cast<std::uintptr_t>(Require()); }

    // Hands ownership to the caller (typically a Scintilla editor via
    // SCI_SETILEXER, which will call Release() itself later). After this,
    // the wrapper no longer touches the lexer.
    std::uintptr_t Detach() {
        const std::uintptr_t ptr = reinterpret_cast<std::uintptr_t>(Require());
        owned_ = false;
        lexer_ = nullptr;
        return ptr;
    }

    // Releases the lexer now instead of waiting for garbage collection.
    // No-op if already detached or released.
    void ReleaseNow() { ReleaseIfOwned(); }

    const char *Name() { return Require()->GetName(); }
    LanguageIdentifier Identifier() { return static_cast<LanguageIdentifier>(Require()->GetIdentifier()); }

    const char *PropertyNames() { return Require()->PropertyNames(); }
    PropertyType GetPropertyType(const char *name) { return static_cast<PropertyType>(Require()->PropertyType(name)); }
    const char *DescribeProperty(const char *name) { return Require()->DescribeProperty(name); }
    Sci_Position PropertySet(const char *key, const char *value) { return Require()->PropertySet(key, value); }
    const char *PropertyGet(const char *key) { return Require()->PropertyGet(key); }

    const char *DescribeWordListSets() { return Require()->DescribeWordListSets(); }
    Sci_Position WordListSet(int n, const char *wordList) { return Require()->WordListSet(n, wordList); }

    int NamedStyles() { return Require()->NamedStyles(); }
    const char *NameOfStyle(int style) { return Require()->NameOfStyle(style); }
    const char *TagsOfStyle(int style) { return Require()->TagsOfStyle(style); }
    const char *DescriptionOfStyle(int style) { return Require()->DescriptionOfStyle(style); }

    int AllocateSubStyles(int styleBase, int numberStyles) { return Require()->AllocateSubStyles(styleBase, numberStyles); }
    int SubStylesStart(int styleBase) { return Require()->SubStylesStart(styleBase); }
    int SubStylesLength(int styleBase) { return Require()->SubStylesLength(styleBase); }
    int StyleFromSubStyle(int subStyle) { return Require()->StyleFromSubStyle(subStyle); }
    int PrimaryStyleFromStyle(int style) { return Require()->PrimaryStyleFromStyle(style); }
    void FreeSubStyles() { Require()->FreeSubStyles(); }
    void SetIdentifiers(int style, const char *identifiers) { Require()->SetIdentifiers(style, identifiers); }
    int DistanceToSecondaryStyles() { return Require()->DistanceToSecondaryStyles(); }
    const char *GetSubStyleBases() { return Require()->GetSubStyleBases(); }

    LineEndType GetLineEndTypesSupported() { return static_cast<LineEndType>(Require()->LineEndTypesSupported()); }

    // Lexer-specific opaque escape hatch; treats the void* in/out the same
    // way Pointer()/Detach() already treat the ILexer5* itself.
    std::uintptr_t PrivateCall(int operation, std::uintptr_t pointer) {
        void *result = Require()->PrivateCall(operation, reinterpret_cast<void *>(pointer));
        return reinterpret_cast<std::uintptr_t>(result);
    }

private:
    ILexer5 *Require() const {
        if (!lexer_) {
            throw std::runtime_error("Lexer has been detached or released; this instance no longer owns an ILexer5");
        }
        return lexer_;
    }

    void ReleaseIfOwned() {
        if (owned_ && lexer_) {
            lexer_->Release();
        }
        lexer_ = nullptr;
    }

    ILexer5 *lexer_;
    bool owned_ = true;
};

std::unique_ptr<Lexer> CreateLexerWrapper(const char *name) {
    ILexer5 *lexer = ::CreateLexer(name);
    if (!lexer) {
        return nullptr;
    }
    return std::make_unique<Lexer>(lexer);
}

std::string GetLexerNameWrapper(unsigned int index) {
    char buffer[256];
    ::GetLexerName(index, buffer, sizeof(buffer));
    return std::string(buffer);
}

